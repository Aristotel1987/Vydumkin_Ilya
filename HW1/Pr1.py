x=int(input('Введите первое число'))
y=int(input('Введите второе число'))
print(f'Сумма чисел равна {x+y}')
print(F'Разность {x} и {y} равна {x-y}')
print(f'Произведение {x} и{y} равно {x*y}')
print(f'Частное от деления {x} на {y} равно {x/y}')
print(f'Остаток от деления {x} на {y} равен {x%y}')
print(f'{x} в степени {y} равно {x**y}')